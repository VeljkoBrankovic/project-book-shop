/* * * mysql hostname ** */
$hostname = "localhost";
/* * * mysql username ** */
$username = "root";
/* * * mysql password ** */
$password = "";
/* * * mysql database name ** */
$database = "books";

try {
    $connection = new PDO("mysql:host=$hostname;dbname=$database;charset=utf8", $username, $password);
    $connection->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

    foreach ($all_books as $book) {
        $sqlQueryString = "INSERT INTO products (title, author, category, description, img, price, stock, barcode)"
                        . " VALUES (" . ":title" . ", " . ":author" . ", " . ":category" . ", " . ":description" . ", " . ":img" . ", " . ":price" . ", " . ":stock" . ", " . ":barcode" . ")";
        $statement = $connection->prepare($sqlQueryString);
        $params = [
            'title' => $book["title"],
            'author' => $book["author"],
            'category' => $book["category"],
            'description' => $book["description"],
            'img' => $book["img"],
            'price' => $book["price"],
            'stock' => $book["stock"],
            'barcode' => $book["barcode"],
        ];
        $status = $statement->execute($params);
    }
} catch (PDOException $exception) {
    echo "Something went wrong: " . $exception->getMessage() . "<br>";
}
    